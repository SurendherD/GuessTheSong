function startTimer(){
   Time= setInterval(()=>{
        timeRemaining--;
        Timer.innerText=timeRemaining;
        if(timeRemaining==0){
            gameOver();
        }
    },1000);
}

function stopTimer(){
    clearInterval(Time);
    timeRemaining=30;
    Timer.innerHTML=timeRemaining;
}

